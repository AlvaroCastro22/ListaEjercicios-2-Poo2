    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        int selectedRow = tableModel.getSelectedRow();
        if(selectedRow != -1){
        int taskId = (int) tableModel.getValueAt(selectedRow, 0);
        taskController.deleteTask(taskId);
        updateTaskTable();
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione una tarea de la lista.");
        }
    }//GEN-LAST:event_EliminarActionPerformed

    /**
     * @param args the command line arguments
     */
    
    private void updateTaskTable() {
        DefaultTableModel model = (DefaultTableModel) tableModel.getModel();
        model.setRowCount(0);
        List<Task> tasks = taskController.getAllTasks();

        for (Task task : tasks) {
            model.addRow(new Object[]{task.getId(), task.getName(), task.isCompleted() ? "SÃ­" : "No",task.getDescripcion(),task.getTipo().name()});
        }
    }
